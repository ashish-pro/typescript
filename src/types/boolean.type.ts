// Boolean values are supported by both JavaScript and TypeScript and stored as true/false values.

let booleanValue: boolean = true;

// Literal values
booleanValue = false;

// expression of true or false
booleanValue = typeof "abc" == "string";
booleanValue = 1 > 0;

booleanValue = {};
booleanValue = "New York";

booleanValue = null;
booleanValue = undefined;